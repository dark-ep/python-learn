# abs
print(abs(100))
print(abs(-20))
print(abs(12.34))

# max
print(max(1, 3, 4, 6, 7, 2, 3, 2.5))

# 数据类型转换
print(int('123'))
print(int(12.34))
print(float('12.34'))
print(str(1.23))
print(str(100))
print(bool(1))
print(bool(''))

# hex
print(hex(255))
print(hex(1000))

# 函数指向
a = abs
print(a(-1))
